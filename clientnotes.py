#The Four Square Method:

#SQUARE 1: INCOME
#Rental Income (our primary income), Laundry Income, Storage, Misc
#Example: Rental Income = $2000/month on a duplex with no laundry, no storage, no misc
    #Total monthly income = $2000/month

#SQUARE 2: EXPENSES
    #Tax = $150
    #Insurance = $100
    #Utilities (electric,water,garbage,gas) = 0$ (Tenant pays for it) 
    #HOA = $0
    #Lawn/Snow care = $0 
    #Vacancy = (5% of Rental Income) $100 - that someday this home will be vacant
    #Repairs = $100
    #Capital Expenditures = $100 
    #Property Management = $200
    #MORTGAGE = $860
#TOTAL = $1610

#SQUARE 3: CASH FLOW (How much extra money I have every month?)
    # Cash Flow = Income - Expenses
    # $390 = $2000 - $1610
    # Total Monthly Cash Flow: $390

#SQUARE 4: CASH ON CASH RETURN ON INVESTMENT (ROI)
    #Down Payment: 20% of 200,000 = $40,000
    #Closing Costs: $3,000
    #Repair/Rehab Budget: $7,000
    #Misc/Other: $0
    #TOTAL INVESTMENT = $50,000
    #ROI CALCULATOR: 
    #1 Need Annual Cash Flow: 390 * 12 = $4680
    #2 (Annual Cash Flow / Total Investment) * 100 
        # (4680/50,000) * 100 = 9.36%
    #3 CASH ON CASH ROI = 9.36%



